function makeDate(daysAgo: number, hour = 10, minute = 30): string {
  return setMinutes(setHours(subDays(new Date(), daysAgo), hour), minute).toISOString()
}

function calcGross(entry: number, exit: number, qty: number, dir: 'long' | 'short'): number {
  return parseFloat(((exit - entry) * qty * (dir === 'long' ? 1 : -1)).toFixed(2))
}

